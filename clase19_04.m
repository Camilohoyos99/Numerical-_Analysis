x = [0 0 0 0];
x1= @(x2,x3) (1/10)* x2 -(1/5)*x3+(3/5);
x2=@(y1,x3,x4) (1/11)*y1 +(1/11)*x3-(3/11)*x4+(25/11);
x3=@(x1,x2,x4)(-1/5)*x1+(1/10)*x2 +(1/10)*x4 -(11/10);
x4 =@(x2,x3)(-3/8)*x2+(1/8)*x3 +(15/8);

error = inf;
k = 0;
while  error>0.003
    xk1=x;
    x(1)= x1(x(2),x(3));
    x(2)= x2(x(1),x(3),x(4));
    x(3)=x3(x(1),x(2),x(4));
    x(4)= x4(x(2),x(3));
    error= max(x - xk1)/max(x);
    k=k+1;
end
    
disp(k)
disp(error)
disp(x)
